public class OtpData {

    private String status;
    private String keymatch;
    private String otp_start;
    private String message;

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getKeymatch() {
        return keymatch;
    }

    public void setKeymatch(String keymatch) {
        this.keymatch = keymatch;
    }

    public String getOtp_start() {
        return otp_start;
    }

    public void setOtp_start(String otp_start) {
        this.otp_start = otp_start;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

}
